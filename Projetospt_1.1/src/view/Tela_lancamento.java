    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        
        int func = 0;
        double sal = 0;
        
        if (edHorasTrabalhadas.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o nome do funcionario!");
            edHorasTrabalhadas.requestFocus();
            return;
        }
        
        if (edPeriodo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o cpf!");
            edPeriodo.requestFocus();
            return;
        }

        if (edValorHora.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o RG!");
            edValorHora.requestFocus();
            return;
        }
        
       
        
        
       try {
        if (cbFuncionario.getSelectedIndex() >= 0){ //alterar
            Funcionarios funcionario = (Funcionarios) cbFuncionario.getSelectedItem();
            edValorHora.setText(String.valueOf(funcionario.getValorHora()));
            PagMensal pagMensal = new PagMensal();
            
            pagMensal.setId_funcioanrio(funcionario.getId());
            pagMensal.setPeriodo(formato.parse(edPeriodo.getText()));
            pagMensal.setSalario(calc.calcularPagamento(Double.parseDouble(edValorHora.getText()), Double.parseDouble(edHorasTrabalhadas.getText())));
            
            func = pagMensal.getId_funcioanrio();
            sal = pagMensal.getSalario();
            
            srv.update(pagMensal);
           
        }
        else { //gravar
               PagMensal pagamento = new PagMensal(Integer.parseInt(edHorasTrabalhadas.getText()), func, sal, formato.parse(edPeriodo.getText()));
               srv.insert(pagamento);
               limparTela();
               
             }
        limparTela();
        this.atualizarListaFuncionario();
          
        } catch (SQLException ex) {
            Logger.getLogger(Tela_Cadastro.class.getName()).log(Level.SEVERE, null, ex);
            limparTela();
        } catch (ParseException ex) {
            Logger.getLogger(Tela_lancamento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2MouseClicked

