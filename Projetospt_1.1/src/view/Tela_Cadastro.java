    private void btSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSalvarMouseClicked
        if (edNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o nome do funcionario!");
            edNome.requestFocus();
            return;
        }
        
        if (edCpf.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o cpf!");
            edCpf.requestFocus();
            return;
        }

        if (edRg.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o RG!");
            edRg.requestFocus();
            return;
        }
        
        if (edValorHora.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o valor da hora!");
            edValorHora.requestFocus();
            return;
        }
        
        if (edStatus.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o status do funcionario!");
            edStatus.requestFocus();
            return;
        }
        
        if (edDataNascimento.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe a data de nascimento do funcionario!");
            edDataNascimento.requestFocus();
            return;
        }
        
        if (edGenero.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o genero do funcionario!");
            edGenero.requestFocus();
            return;
        }
        
        
       try {
        if (cbFuncionario.getSelectedIndex() >= 0){ //alterar
            Funcionarios funcionario = (Funcionarios) cbFuncionario.getSelectedItem();
            
            funcionario.setNome(edNome.getText());
            funcionario.setGenero(edGenero.getText());
            funcionario.setStatus(edStatus.getText());
            funcionario.setData_nascimento((Date)formato.parse(edDataNascimento.getText()));
            funcionario.setCpf(edCpf.getText());
            funcionario.setRg(edRg.getText());
            funcionario.setValorHora(Double.parseDouble(edValorHora.getText()));
            
            srvFuc.update(funcionario);
           
        }
        else { //gravar
               Funcionarios funcionario = new Funcionarios(edNome.getText(),edGenero.getText(), (Date)formato.parse(edDataNascimento.getText()), edStatus.getText(), edRg.getText(), edCpf.getText(), Double.parseDouble(edValorHora.getText()) );
               srvFuc.insert(funcionario);
               limparTela();
               
             }
        limparTela();
        this.atualizarListaFuncionario();
          
        } catch (SQLException ex) {
            Logger.getLogger(Tela_Cadastro.class.getName()).log(Level.SEVERE, null, ex);
            limparTela();
        } catch (ParseException ex) {
            Logger.getLogger(Tela_Cadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btSalvarMouseClicked

    private void cbFuncionarioItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbFuncionarioItemStateChanged
         if (cbFuncionario.getItemCount() < 0){
            return;
        }
        
        if (cbFuncionario.getSelectedIndex() < 0){
            return;
        } 
        
        Funcionarios funcionario = (Funcionarios) cbFuncionario.getSelectedItem();
        edNome.setText(funcionario.getNome());
        edStatus.setText(funcionario.getStatus());
        edDataNascimento.setText(formato.format(funcionario.getData_nascimento()));
        edGenero.setText(funcionario.getGenero());
        edCpf.setText(funcionario.getCpf());
        edRg.setText(funcionario.getRg());
        edValorHora.setText(String.valueOf(funcionario.getValorHora()));
        
        btLimpar.setEnabled(true);
    }//GEN-LAST:event_cbFuncionarioItemStateChanged

