    public void carregarComboBoxFuncionarios() throws SQLException{
        try {
            if (cbFuncionario.getItemCount() > 0){
               cbFuncionario.removeAllItems();
            }
        
          ArrayList<Funcionarios> lista = srvFuc.getFuncionarioByLista();
          
          for (Funcionarios u:lista){
            cbFuncionario.addItem(u);
          }      
        
          this.limparTela();
          
        } catch (SQLException ex) {
            Logger.getLogger(Tela_Cadastro.class.getName()).log(Level.SEVERE, null, ex);
          };          
        
    }
    
